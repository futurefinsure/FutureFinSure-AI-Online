// ฟังก์ชันหาอัตราเบี้ยใกล้เคียง
function getNearestRate(table, age) {
  if (table[age]) return table[age];
  const keys = Object.keys(table).map(Number).sort((a,b)=>a-b);
  let closest = keys[0];
  for (const k of keys) {
    if (Math.abs(k-age) < Math.abs(closest-age)) closest = k;
  }
  return table[closest];
}

// ฟังก์ชันคำนวณเบี้ย Life Insurance
function calcLifePremium(plan, age, sum, gender) {
  const tables = {'10':T_10PAY,'15':T_15PAY,'20':T_20PAY};
  const table = tables[plan];
  if (!table || !table[age]) return null;
  const gi = gender==='male' ? 0 : 1;
  const rate = table[age][gi];
  const discRate = plan==='20'
    ? (sum>=600000?2:sum>=250000?1:0)
    : (sum>=500000?1:0);
  const units = sum/1000;
  const annualGross = rate * units;
  const discAmount = discRate * units;
  const annualNet = annualGross - discAmount;
  return { annualGross, discAmount, annualNet, monthly: annualNet/12, payYears: parseInt(plan) };
}

// ฟังก์ชันคำนวณเบี้ย CI SuperCare
function calcCIPremium(plan, age, sum, gender) {
  const table = plan==='10' ? T_CI10 : T_CI20;
  const row = getNearestRate(table, age);
  if (!row) return null;
  const gi = gender==='male' ? 0 : 3;
  const rate = row[gi];
  const discRate = plan==='10'
    ? (sum>=5000000?3.5:sum>=1000000?2.5:0)
    : (sum>=5000000?2.0:sum>=1000000?1.5:sum>=800000?1.0:sum>=500000?0.5:0);
  const units = sum/1000;
  const annualNet = (rate-discRate)*units;
  return { annualNet, monthly: annualNet/12, payYears: parseInt(plan), coverCI: sum, coverMild: sum*0.2 };
}

// ฟังก์ชันคำนวณเบี้ย Senior Happy
function calcSeniorPremium(age, sum, gender) {
  if (age<50||age>70||sum<50000||sum>200000) return null;
  const row = T_SENIOR[age];
  if (!row) return null;
  const gi = gender==='male' ? 0 : 1;
  const rate = row[gi];
  const annualNet = rate*(sum/1000);
  return { annualNet, monthly: annualNet/12, payYears: 90-age };
}

